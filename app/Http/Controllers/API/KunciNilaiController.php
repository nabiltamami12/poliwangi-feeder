            $this->error = $e;
        }
        return response()->json([
            "status" => $this->status,
            "data" => $this->data,
            "error" => $this->error
        ]);
    }

    public function change_status($id)
    {
        try {
            $kunciNilai = KunciNilai::where('id', $id)->first();
            if ($kunciNilai->status == 1) {
                KunciNilai::where('id', $id)->update(['status' => 0]);
            } else {
                KunciNilai::where('status', 1)->update(['status' => 0]);
                KunciNilai::where('id', $id)->update(['status' => 1]);
            }

            $this->data = [];
            $this->status = "success";
        } catch (QueryException $e) {
            $this->status = "failed";
            $this->error = $e;
        }
        return response()->json([
            "status" => $this->status,
            "data" => $this->data,
            "error" => $this->error
        ]);
    }

